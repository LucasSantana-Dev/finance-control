package com.finance_control.shared.enums;

/**
 * Enumeration representing the types of financial goals.
 * Defines different categories of financial objectives that users
 * can set and track in the system.
 */
public enum GoalType {
    /** Emergency fund for unexpected expenses */
    EMERGENCY_FUND,
    
    /** General savings goal */
    SAVINGS,
    
    /** Vacation or travel savings */
    VACATION,
    
    /** Purchase of specific items */
    PURCHASE,
    
    /** Investment goals */
    INVESTMENT,
    
    /** Debt repayment goals */
    DEBT_PAYOFF,
    
    /** Other miscellaneous goals */
    OTHER
} 